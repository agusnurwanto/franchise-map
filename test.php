// All logic goes in this class
class FranchiseSearch
{
    public $region_mappings;
    public $franchises;
    // Receives string in CSV format
    function initialize($franchises_string, $region_mappings_string)
    {
        // Assign $region_mappings
        $this->region_mappings = $this->parseRegionMapping($this->parseCSV($region_mappings_string));
        
        // Assign $franchises
        $this->franchises = $this->parseFranchises($this->parseCSV($franchises_string));
    }
    
    // Should return an ordered array (by name) of franchises for this postal code with all their information
    function search($postal_code)
    {
        // Postal code 14410 should find 3 locations.
        // If sorted alphabetically, second location name is "Prohaska, Gibson and Rolfson"
        // website: auto-service.co/yw-159-d
        // city: Adams Basin
        // state: NY
        $data = array();
        if(!empty($this->region_mappings[$postal_code])){
            $region = $this->region_mappings[$postal_code];
            if(!empty($this->franchises[$region['region_code']])){
                $franchises = $this->franchises[$region['region_code']];
                ksort($franchises);
                $no = 0;
                $data = array();
                foreach($franchises as $v){
                    $data[$no] = array_merge($v, $region);
                    $no++;
                }
            }
        }
        
        // Should return array of arrays
        return $data;
    }
    
    function parseCSV($data){
        $lines = explode(PHP_EOL, $data);
		$array = array();
		foreach ($lines as $line) {
		    $array[] = str_getcsv($line);
		}
        $array[0] = str_replace(';', '', $array[0]);
        return $array;
    }
    
    function parseRegionMapping($array){
        $data = array();
        foreach ($array as $k => $v) {
			if($k==0 || empty($v[0]))
				continue;
			foreach ($v as $key2 => $value) {
				$data[$v[0]][$array[0][$key2]] = $value;
			}
		}
		return $data;
    }
    
    function parseFranchises($array){
        $data = array();
        foreach ($array as $k => $v) {
			if($k==0 || empty($v[0]))
				continue;
			$regions = explode(',', str_replace(';', '', $v[5]));
			foreach ($regions as $val) {
				foreach ($v as $key2 => $value) {
					$data[$val][$v[1]][$array[0][$key2]] = $value;
				}
			}
		}
		return $data;
    }
}
